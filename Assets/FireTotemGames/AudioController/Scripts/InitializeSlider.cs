using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;
using FTG.AudioController;

public class InitializeSlider : MonoBehaviour
{
    /* ======================================================================================================================== */
    /* VARIABLE DECLARATIONS                                                                                                    */
    /* ======================================================================================================================== */

    private enum SliderType
    {
        musicVolume,
        soundVolume
    }

    [SerializeField] private SliderType sliderType;

    /* ======================================================================================================================== */
    /* UNITY CALLBACKS                                                                                                          */
    /* ======================================================================================================================== */

    private void Start()
    {
        switch (sliderType)
        {
            case SliderType.musicVolume:
                GetComponent<Slider>().value = Mathf.Pow(10f,(AudioController.Instance.GetMusicVolume()) / 20f);
                break;
            case SliderType.soundVolume:
                GetComponent<Slider>().value = Mathf.Pow(10f, (AudioController.Instance.GetSoundVolume()) / 20f);
                break;
            default:
                break;
        }
    }

    private void Update()
    {

    }

    /* ======================================================================================================================== */
    /* PRIVATE FUNCTIONS                                                                                                        */
    /* ======================================================================================================================== */

    /* ======================================================================================================================== */
    /* PUBLIC FUNCTIONS                                                                                                         */
    /* ======================================================================================================================== */

    /* ======================================================================================================================== */
    /* EVENT CALLERS                                                                                                            */
    /* ======================================================================================================================== */

    /* ======================================================================================================================== */
    /* EVENT LISTENERS                                                                                                          */
    /* ======================================================================================================================== */

}