using UnityEngine;
using UnityEngine.UI;

public class CanvasScaleController : MonoBehaviour
{
    /* ======================================================================================================================== */
    /* VARIABLE DECLARATIONS                                                                                                    */
    /* ======================================================================================================================== */

    [SerializeField] private ScreenOrientation screenOrientation;
    [SerializeField] private float defaultRatio = 16f / 9f;

    private enum ScreenOrientation
    {
        Horizontal,
        Vertical
    }
    
        /* ======================================================================================================================== */
    /* UNITY CALLBACKS                                                                                                          */
    /* ======================================================================================================================== */

    private void Start ()
    {
        SetScaleMode();
    }
    
    /* ======================================================================================================================== */
    /* PRIVATE FUNCTIONS                                                                                                        */
    /* ======================================================================================================================== */
    
    /* ======================================================================================================================== */
    /* PRIVATE FUNCTIONS                                                                                                        */
    /* ======================================================================================================================== */

    private void SetScaleMode()
    {
        float matchWidthOrHeight = 0f;
        float ratio = 0f;

        switch (screenOrientation)
        {
            case ScreenOrientation.Horizontal:
                ratio = ((float)Screen.width / Screen.height) / defaultRatio;
                break;
            case ScreenOrientation.Vertical:
                ratio = ((float)Screen.height / Screen.width) / defaultRatio;
                break;
            default:
                break;
        }

        matchWidthOrHeight = ratio < 1f ? 0.0f : 1.0f;

        gameObject.GetComponent<CanvasScaler>().matchWidthOrHeight = matchWidthOrHeight;
    }

    /* ======================================================================================================================== */
    /* PUBLIC FUNCTIONS                                                                                                         */
    /* ======================================================================================================================== */

    /* ======================================================================================================================== */
    /* EVENT CALLERS                                                                                                            */
    /* ======================================================================================================================== */

    /* ======================================================================================================================== */
    /* EVENT LISTENERS                                                                                                          */
    /* ======================================================================================================================== */

}
